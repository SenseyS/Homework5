for (let i = 0; i < 2; i++) {
    for (let j = 0; j < 60; j++) {
        for (p = 0; p < 60; p++) {
            if (j < 10 && p < 10) {
                console.log(0 + i + ':' + 0 + j + ':' + 0 + p);
            } else {
                console.log(0 + i + ':' + j + ':' + p);
            }
        }

    }
}